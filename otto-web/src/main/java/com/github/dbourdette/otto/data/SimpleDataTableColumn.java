package com.github.dbourdette.otto.data;

/**
 * A column in a {@link SimpleDataTable}
 */
public class SimpleDataTableColumn {

    private String title;

    public SimpleDataTableColumn(String title) {
        this.title = title;
    }

    public String getTitle() {
        return title;
    }

    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + ((title == null) ? 0 : title.hashCode());
        return result;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj)
            return true;
        if (obj == null)
            return false;
        if (getClass() != obj.getClass())
            return false;
        SimpleDataTableColumn other = (SimpleDataTableColumn) obj;
        if (title == null) {
            if (other.title != null)
                return false;
        } else if (!title.equals(other.title))
            return false;
        return true;
    }

    @Override
    public String toString() {
        return title;
    }
}
